const H = WORLD_HEIGHT;

function spreadSlimes(fromX, toX, y, count, variant = "green") {
  const step = (toX - fromX) / Math.max(1, count - 1);
  return Array.from({ length: count }, (_, i) => ({
    x: fromX + i * step,
    y,
    patrolDistance: 70 + (i % 3) * 15,
    variant,
  }));
}

function spreadBats(fromX, toX, y, count) {
  const step = (toX - fromX) / Math.max(1, count - 1);
  return Array.from({ length: count }, (_, i) => ({
    x: fromX + i * step,
    y,
    amp: 40 + (i % 4) * 12,
    speed: 0.002 + (i % 3) * 0.0004,
  }));
}

function spreadSpitters(fromX, toX, y, count) {
  const step = (toX - fromX) / Math.max(1, count - 1);
  return Array.from({ length: count }, (_, i) => ({
    x: fromX + i * step,
    y,
    patrolDistance: 55,
  }));
}

/** Plattformen [tileX, tileY, width] — Musterwiederholung für lange Stages */
function platformsStandard(wTiles) {
  return [
    [12, 40, 11],
    [28, 34, 10],
    [44, 38, 9],
    [58, 30, 12],
    [72, 36, 11],
    [88, 32, 10],
    [102, 28, 13],
    [118, 35, 10],
    [134, 31, 12],
    [150, 27, 11],
    [Math.min(165, wTiles - 20), 33, 10],
  ].filter(([sx]) => sx < wTiles - 5);
}

function holesSparse(wTiles) {
  return [
    [40, 48],
    [90, 98],
    [140, 152],
  ].filter(([a]) => a < wTiles);
}

